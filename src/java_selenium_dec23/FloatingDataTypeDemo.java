package java_selenium_dec23;

public class FloatingDataTypeDemo {
public static void main(String[] args) {
	// Whole number
	// floating - 
	// 75.4
	// 4.5
	
	// float - 4 byte
	// double - 8 byte
	
	float fl = ((float)12)/5; // 2.4
	System.out.println(fl);
	
	double f2 = 12_4_4.34_53D;
	System.out.println(f2);
	
	// 6 datatype
	// byte - 1 byte
//	short - 2 
//	int - 4
//	long - 8
//	float - 4
//	double - 8
	
	
}
}
